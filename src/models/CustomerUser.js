class CustomerUser {
    constructor(
        id, 
        email, 
        password, 
        mbtiType, 
        kearseyPref, 
        responsibilityPref, 
        recognitionPref, 
        respectPref, 
        reassurancePref,
        resiliencePref,
    ){
        this.id = id;
        this.email = email;
        this.password = password;
        this.mbtiType = mbtiType;
        this.kearseyPref = kearseyPref;
        this.responsibilityPref = responsibilityPref;
        this.recognitionPref = recognitionPref;
        this.respectPref = respectPref;
        this.reassurancePref = reassurancePref;
        this.resiliencePref = resiliencePref;
    }
}